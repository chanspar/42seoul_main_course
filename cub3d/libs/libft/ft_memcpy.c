#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	idx;

	idx = 0;
	while (idx < n)
	{
		((char *)dst)[idx] = ((const char *)src)[idx];
		idx++;
	}
	return (dst);
}
